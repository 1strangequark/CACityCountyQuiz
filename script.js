// Array of cities for random selection
const cities = Object.keys(cityCountyMapping);
let currentCity = "";

// Function to pick a random city
function getRandomCity() {
    const randomIndex = Math.floor(Math.random() * cities.length);
    return cities[randomIndex];
}

// Function to set the card content
function setCardContent(city) {
    document.getElementById('card-title').textContent = "City";
    document.getElementById('city-name').textContent = city;
    document.getElementById('county-name').textContent = "";
    document.getElementById('county-name').classList.add('hidden');
}

// Function to reveal the county
function revealCounty() {
    document.getElementById('card-title').textContent = "City & County";
    document.getElementById('county-name').textContent = cityCountyMapping[currentCity];
    document.getElementById('county-name').classList.remove('hidden');
    document.getElementById('reveal-button').textContent = 'Next';
}

// Function to show the next city
function showNextCity() {
    document.getElementById('reveal-button').textContent = 'Reveal';
    currentCity = getRandomCity();
    setCardContent(currentCity);
}

// Initial setup
currentCity = getRandomCity();
setCardContent(currentCity);

document.getElementById('reveal-button').addEventListener('click', function() {
    if (this.textContent === 'Reveal') {
        revealCounty();
    } else {
        showNextCity();
    }
});
